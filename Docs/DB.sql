(
  ID_VIAJEROS NUMBER,
  TIPO_ID_VIAJEROS VARCHAR2(255 BYTE),
  ID_VUELO VARCHAR2(255 BYTE),
  HORA_SALIDA_VIAJE DATE,
  CONSTRAINT M_VIAJES_M_VIAJEROS_PK PRIMARY KEY (ID_VIAJEROS, TIPO_ID_VIAJEROS,ID_VUELO,HORA_SALIDA_VIAJE),
