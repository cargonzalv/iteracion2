CREATE TABLE VIAJEROS
(
  ID NUMBER NOT NULL,
  TIPO_ID VARCHAR2(255 BYTE),
  MILLAS_RECORRIDAS DOUBLE PRECISION,
  CONSTRAINT VIAJEROS_PK PRIMARY KEY (ID,TIPO_ID),
