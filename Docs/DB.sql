CREATE TABLE REMITENTES
(
  ID NUMBER NOT NULL, 
  TIPO_ID VARCHAR2(255 BYTE),
  CONSTRAINT REMITENTES_PK PRIMARY KEY (ID,TIPO_ID),
  CONSTRAINT FK_REMITENTES FOREIGN KEY(ID,TIPO_ID) REFERENCES USUARIO(ID,TIPO_ID)
  
);

CREATE TABLE M_VIAJES_M_REMITENTES
(
  ID_REMITENTE NUMBER,
  TIPO_ID_REMITENTE VARCHAR2(255 BYTE),
  ID_VIAJE NUMBER,
  CONSTRAINT VIAJES_DE_REMITENTES_PK PRIMARY KEY (ID_REMITENTE, TIPO_ID_REMITENTE,ID_VIAJE),
