CREATE TABLE CIUDAD
(
  NOMBRE VARCHAR2(255 BYTE) NOT NULL,
  NOMBRE_PAIS VARCHAR2(255 BYTE) NOT NULL,
  CONSTRAINT FK_NOMBRE_PAIS_CIUDAD FOREIGN KEY(NOMBRE_PAIS) REFERENCES PAIS(NOMBRE),
  CONSTRAINT CIUDAD_PK PRIMARY KEY (NOMBRE)
