CREATE TABLE AEROPUERTO
(
  COD_IATA VARCHAR2(255 BYTE) NOT NULL,
  NOMBRE VARCHAR2(255 BYTE) NOT NULL,
  TIPO VARCHAR2(255 BYTE) NOT NULL,
  NOMBRE_CIUDAD VARCHAR2(255 BYTE) NOT NULL,
