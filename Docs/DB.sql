CREATE TABLE CARGA
(
  TRACKING_NUM NUMBER NOT NULL,
  CONTENIDO VARCHAR2(255 BYTE),
  PESO DOUBLE PRECISION,
  VOLUMEN DOUBLE PRECISION,
  CONSTRAINT CARGA_PK PRIMARY KEY (TRACKING_NUM)
);
CREATE TABLE M_VIAJES_M_CARGAS
(
  ID_CARGA NUMBER,
  ID_VUELO VARCHAR2(255 BYTE),
  HORA_SALIDA_VIAJE DATE,
  CONSTRAINT VIAJES_DE_CARGAS_PK PRIMARY KEY (ID_CARGA,ID_VUELO,HORA_SALIDA_VIAJE),
  CONSTRAINT FK_CARGAS_VIAJE FOREIGN KEY(ID_CARGA) REFERENCES CARGA(TRACKING_NUM),
  CONSTRAINT FK_VIAJES_CARGA FOREIGN KEY(ID_VUELO,HORA_SALIDA_VIAJE) REFERENCES VIAJE_CARGA(ID,HORA_SALIDA)
  
);

CREATE TABLE RESERVA
(
  ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
  FECHA DATE,
  FECHA_ACTUAL DATE DEFAULT CURRENT_DATE,
  TIPO_RESERVA VARCHAR2(255 BYTE),
  AERO_SAL VARCHAR2(255 BYTE),
  AERO_LLEG VARCHAR2(255 BYTE),
  CONSTRAINT FK_AEROPUERTOSAL_RESERVA FOREIGN KEY(AERO_SAL) REFERENCES AEROPUERTO(COD_IATA),
  CONSTRAINT CK_TIPO_RESERVA CHECK(TIPO_RESERVA = 'Carga' OR TIPO_RESERVA = 'Viajeros'),
  CONSTRAINT CK_FECHA_RESERVA CHECK(FECHA > FECHA_ACTUAL),
  CONSTRAINT RESERVA_PK PRIMARY KEY (ID)
);

CREATE TABLE RESERVA_VIAJEROS
(
  ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
  ID_RESERVA_GENERAL NUMBER,
  ID_VIAJERO NUMBER NOT NULL,
  TIPO_ID_VIAJERO VARCHAR2(255 BYTE),
  ID_VUELO VARCHAR2(255 BYTE) NOT NULL,
  HORA_SALIDA_VIAJE DATE NOT NULL,
  CLASE VARCHAR2(255 BYTE) NOT NULL,
  CONSTRAINT CK_CLASE_RESERVA CHECK(CLASE = 'Ejecutiva' OR CLASE = 'Economica'),
  CONSTRAINT FK_VUELO_RESERVA_M FOREIGN KEY(ID_VUELO,HORA_SALIDA_VIAJE) REFERENCES VIAJE_VIAJEROS(ID,HORA_SALIDA),
  CONSTRAINT FK_VIAJEROS_RESERVA_MULTIPLES FOREIGN KEY (ID_VIAJERO, TIPO_ID_VIAJERO) REFERENCES VIAJEROS(ID, TIPO_ID),
  CONSTRAINT RESERVA_MULTIPLES_VIAJES_PK PRIMARY KEY (ID),
  CONSTRAINT FK_RESERVA_MULTIPLES_VUELOS FOREIGN KEY(ID_RESERVA_GENERAL) REFERENCES RESERVA(ID)
);


CREATE TABLE RESERVA_CARGA
(
  ID NUMBER,
  ID_VUELO VARCHAR2(255 BYTE) NOT NULL,
  HORA_SALIDA_VIAJE DATE NOT NULL,
  ID_REMITENTE NUMBER NOT NULL,
  TIPO_ID_REMITENTE VARCHAR2(255 BYTE),
  ID_CARGA NUMBER NOT NULL,
  CONSTRAINT FK_VUELO_RESERVA_CARGA FOREIGN KEY (ID_VUELO,HORA_SALIDA_VIAJE) REFERENCES VIAJE_CARGA(ID,HORA_SALIDA),
  CONSTRAINT FK_REMITENTE_RESERVA_CARGA FOREIGN KEY (ID_REMITENTE,TIPO_ID_REMITENTE) REFERENCES REMITENTES(ID,TIPO_ID),
  CONSTRAINT FK_CARGA_RESERVA_CARGA FOREIGN KEY (ID_CARGA) REFERENCES CARGA(TRACKING_NUM),
  CONSTRAINT RESERVA_CARGA_PK PRIMARY KEY (ID),
  CONSTRAINT FK_RESERVA_CARGA FOREIGN KEY(ID) REFERENCES RESERVA(ID)
);


